   public int solutionStack(String S)
   {
      StringTokenizer tokenList = new StringTokenizer(S, " ");
      List<Integer> stack = new ArrayList<Integer>();
      
      
      while (tokenList.hasMoreTokens())
      {
          String nextToken = tokenList.nextToken();
          boolean isValue = false;
          try
          {
             Integer value = Integer.valueOf(nextToken);
             
             stack.add(value);
             isValue = true;
          }
          catch(NumberFormatException nfe)
          {
      
          }
          
          if (!isValue)
          {
             if (nextToken.equals("DUP"))
             {
                if (1 >stack.size()) return -1;
                
                stack.add(stack.get(stack.size() - 1));
             }
             
             if (nextToken.equals("POP"))
             {
                if (1 > stack.size()) return -1;               

                stack.remove(stack.size() - 1);

             }
        
             if (nextToken.equals("+"))
             {
                if (2 > stack.size()) return -1;               
  
                Integer top = stack.remove(stack.size() - 1);
                Integer nextTop = stack.remove(stack.size() - 1);

                if (1048576 < top + nextTop) return -1;
                
                stack.add(top + nextTop);
             }
                      
             if (nextToken.equals("-"))
             {
                if (2 > stack.size()) return -1;
                
                Integer top = stack.remove(stack.size() - 1);
                Integer nextTop = stack.remove(stack.size() - 1);

                if (0 > top - nextTop) return -1;
                
                stack.add(top - nextTop);
             }
          }
          
          return stack.get(stack.size() - 1).intValue();
      }
      
      return -1;

   }

